/**
 * 가중치 표시용 반올림(소수 2자리). 가중치는 0~100 스케일(전체 합 100)이며,
 * 엑셀 수식에서 유래한 무한소수(4.545454…)가 화면 셀·보고서를 넘쳐 이웃 컬럼
 * (계획시작/종료)을 가리는 문제를 방지한다.
 *
 * 0~1 스케일 시절엔 4자리였다(0.0455). 100 스케일에서 같은 유효숫자를 유지하려면
 * 2자리면 충분하다(4.55). 저장값과 임포트/내보내기 라운드트립은 원본 정밀도를
 * 유지하고, 표시만 이 값을 쓴다.
 */
export function roundWeight(w: number): number {
  return Number(w.toFixed(2))
}
